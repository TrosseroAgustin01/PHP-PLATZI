Los enums o "Enums" permiten a un desarrollador definir un tipo personalizado que se limita a uno de un número discreto de valores posibles.

Eso puede ser especialmente útil al definir un modelo de dominio, ya que permite "hacer irrepresentables los estados inválidos".

Las enumeraciones aparecen en muchos idiomas con una variedad de características diferentes.

En PHP, los Enums son un tipo especial de objeto. El Enum en sí mismo es una clase, y sus posibles casos son todos objetos de instancia única de esa clase.

Eso significa que los casos de Enum son objetos válidos y se pueden usar en cualquier lugar donde se pueda usar un objeto, incluidas las comprobaciones de tipo.
